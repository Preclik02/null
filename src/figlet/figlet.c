void print_figlet(const char *text) {
    for (int row = 0; row < FONT_HEIGHT; row++) {
        for (size_t i = 0; i < strlen(text); i++) {
            unsigned char c = text[i];
            
            if (c < MIN_CHAR || c > MAX_CHAR || FONT_DATA[c][row] == NULL) {
                c = ' ';
            }
            
            printf("%s", FONT_DATA[c][row]);
        }
        printf("\n");
    }
}

int main(int argc, char *argv[]) {
    if (argc < 2) {
        printf("Usage: %s <text>\n", argv[0]);
        return 1;
    }

    print_figlet(argv[1]);
    return 0;
}
